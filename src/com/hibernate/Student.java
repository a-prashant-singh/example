package com.hibernate;

public class Student {
	
	private int id;
	private String name;
	private String cllg;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCllg() {
		return cllg;
	}
	public void setCllg(String cllg) {
		this.cllg = cllg;
	}
	
	

}
